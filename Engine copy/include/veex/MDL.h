// ============================================================================
// Runtime Data Structures
// ============================================================================

struct Bone {
    std::string name;
    int32_t parent;
    glm::vec3 position;
    glm::quat rotation;
    glm::mat4 poseToBone;
    int32_t flags;
};

struct MDLVertex {
    glm::vec3 position;
    glm::vec3 normal;
    glm::vec2 texCoord;
    glm::vec3 tangent;
    std::array<float, 3> boneWeights;
    std::array<int8_t, 3> boneIds;
    uint8_t numBones;
};

struct MeshData {
    int32_t materialIndex;
    std::vector<MDLVertex> vertices;
    std::vector<uint32_t> indices;
    int32_t bodyPart;
    int32_t model;
    int32_t mesh;
};

struct AnimationSequence {
    std::string name;
    std::string activityName;
    int32_t activity;
    int32_t flags;
    float frameRate;
    int32_t numFrames;
    int32_t numBlends;
    int32_t animIndex;
};

struct SkinFamily {
    std::vector<int16_t> textures;  // Texture indices per skin reference
};

// ============================================================================
// Loaded MDL Model
// ============================================================================
class MDLModel {
public:
    MDLModel();
    ~MDLModel();

    // Load from file path (will use FileSystem to resolve)
    bool LoadFromFile(const std::string& path, const GameInfo& game);
    
    // Load from buffer (for VPK support)
    bool LoadFromBuffer(const std::vector<char>& buffer);

    // Accessors
    const std::vector<Bone>& GetBones() const { return m_bones; }
    const std::vector<MDLVertex>& GetVertices() const { return m_vertices; }
    const std::vector<uint32_t>& GetIndices() const { return m_indices; }
    const std::vector<std::string>& GetMaterialNames() const { return m_materialNames; }
    const std::vector<AnimationSequence>& GetSequences() const { return m_sequences; }
    const std::vector<MeshData>& GetMeshes() const { return m_meshes; }
    const std::vector<std::string>& GetTextureDirs() const { return m_textureDirs; }
    const std::vector<SkinFamily>& GetSkinFamilies() const { return m_skinFamilies; }
    
    int32_t GetNumBones() const { return static_cast<int32_t>(m_bones.size()); }
    int32_t GetNumVertices() const { return static_cast<int32_t>(m_vertices.size()); }
    int32_t GetNumIndices() const { return static_cast<int32_t>(m_indices.size()); }
    int32_t GetNumMaterials() const { return static_cast<int32_t>(m_materialNames.size()); }
    int32_t GetNumSequences() const { return static_cast<int32_t>(m_sequences.size()); }
    int32_t GetNumMeshes() const { return static_cast<int32_t>(m_meshes.size()); }
    int32_t GetNumSkinFamilies() const { return static_cast<int32_t>(m_skinFamilies.size()); }

    // Animation
    int32_t FindSequence(const std::string& name) const;
    std::string GetSequenceName(int32_t index) const;

    // Model info
    const std::string& GetName() const { return m_name; }
    float GetMass() const { return m_mass; }
    uint32_t GetFlags() const { return m_flags; }
    const glm::vec3& GetEyePosition() const { return m_eyePosition; }
    const glm::vec3& GetBoundingBoxMin() const { return m_bbMin; }
    const glm::vec3& GetBoundingBoxMax() const { return m_bbMax; }
    const glm::vec3& GetHullMin() const { return m_hullMin; }
    const glm::vec3& GetHullMax() const { return m_hullMax; }
    int32_t GetChecksum() const { return m_checksum; }
    int32_t GetVersion() const { return m_version; }
    
    // Surface property
    const std::string& GetSurfaceProp() const { return m_surfaceProp; }

private:
    bool ParseMDL(const uint8_t* data, size_t size);
    bool ParseVVD(const std::vector<char>& buffer);
    bool ParseVTX(const std::vector<char>& buffer);
    
    bool ParseHeader(const uint8_t* data, size_t size);
    bool ParseStudioHdr2(const uint8_t* data, size_t size);
    bool ParseBones(const uint8_t* data, size_t size);
    bool ParseTextures(const uint8_t* data, size_t size);
    bool ParseTextureDirs(const uint8_t* data, size_t size);
    bool ParseSkinFamilies(const uint8_t* data, size_t size);
    bool ParseBodyParts(const uint8_t* data, size_t size);
    bool ParseSequences(const uint8_t* data, size_t size);
    bool ParseHitboxSets(const uint8_t* data, size_t size);
    bool ParseSurfaceProp(const uint8_t* data, size_t size);
    bool ParseAttachments(const uint8_t* data, size_t size);
    
    bool BuildGeometry();
    
    std::string ResolveString(const uint8_t* base, int32_t offset) const;
    bool BoundsCheck(int32_t offset, size_t sizeNeeded, size_t fileSize) const;
    
    // Header data
    studiohdr_t m_header;
    std::unique_ptr<studiohdr2_t> m_header2;
    
    std::string m_name;
    float m_mass;
    uint32_t m_flags;
    int32_t m_checksum;
    int32_t m_version;
    glm::vec3 m_eyePosition;
    glm::vec3 m_bbMin, m_bbMax;
    glm::vec3 m_hullMin, m_hullMax;
    std::string m_surfaceProp;
    
    // Parsed data
    std::vector<Bone> m_bones;
    std::vector<MDLVertex> m_vertices;
    std::vector<uint32_t> m_indices;
    std::vector<std::string> m_materialNames;
    std::vector<std::string> m_textureDirs;
    std::vector<AnimationSequence> m_sequences;
    std::vector<SkinFamily> m_skinFamilies;
    std::vector<MeshData> m_meshes;
    
    // Raw file data for parsing
    std::vector<char> m_mdlData;
    std::vector<char> m_vvdData;
    std::vector<char> m_vtxData;
    const uint8_t* m_baseData = nullptr;
};

