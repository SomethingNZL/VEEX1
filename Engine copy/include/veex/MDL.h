// Magic number: "IDST" in little-endian
constexpr int32_t MDL_IDST = (('T' << 24) | ('S' << 16) | ('D' << 8) | ('I' << 0)); // 0x54534449

// Supported MDL versions
constexpr int32_t MDL_VERSION_44 = 44; // Source 2004
constexpr int32_t MDL_VERSION_45 = 45; // Source 2006
constexpr int32_t MDL_VERSION_46 = 46; // Source 2007
constexpr int32_t MDL_VERSION_47 = 47; // Source 2009
constexpr int32_t MDL_VERSION_48 = 48; // Source 2011
constexpr int32_t MDL_VERSION_49 = 49; // Source 2013 (SDK2013)

// VVD (Vertex Data) magic
constexpr int32_t VVD_IDSV = (('V' << 24) | ('S' << 16) | ('D' << 8) | ('I' << 0)); // 0x56534449
constexpr int32_t VVD_VERSION = 4;

// VTX version for Source 2013
constexpr int32_t VTX_VERSION_7 = 7;

// ============================================================================
// Model Flags (from studio.h)
// ============================================================================
enum MDLFlags : uint32_t {
    STUDIOHDR_FLAGS_AUTOGENERATED_HITBOX          = 0x00000001,
    STUDIOHDR_FLAGS_USES_ENV_CUBEMAP              = 0x00000002,
    STUDIOHDR_FLAGS_FORCE_OPAQUE                  = 0x00000004,
    STUDIOHDR_FLAGS_TRANSLUCENT_TWOPASS           = 0x00000008,
    STUDIOHDR_FLAGS_STATIC_PROP                   = 0x00000010,
    STUDIOHDR_FLAGS_USES_FB_TEXTURE               = 0x00000020,
    STUDIOHDR_FLAGS_HASSHADOWLOD                  = 0x00000040,
    STUDIOHDR_FLAGS_USES_BUMPMAPPING              = 0x00000080,
    STUDIOHDR_FLAGS_USE_SHADOWLOD_MATERIALS       = 0x00000100,
    STUDIOHDR_FLAGS_OBSOLETE                      = 0x00000200,
    STUDIOHDR_FLAGS_UNUSED                        = 0x00000400,
    STUDIOHDR_FLAGS_NO_FORCED_FADE                = 0x00000800,
    STUDIOHDR_FLAGS_FORCE_PHONEME_CROSSFADE       = 0x00001000,
    STUDIOHDR_FLAGS_CONSTANT_DIRECTIONAL_LIGHT_DOT = 0x00002000,
    STUDIOHDR_FLAGS_FLEXES_CONVERTED              = 0x00004000,
    STUDIOHDR_FLAGS_BUILT_IN_PREVIEW_MODE         = 0x00008000,
    STUDIOHDR_FLAGS_AMBIENT_BOOST                 = 0x00010000,
    STUDIOHDR_FLAGS_DO_NOT_CAST_SHADOWS           = 0x00020000,
    STUDIOHDR_FLAGS_CAST_TEXTURE_SHADOWS          = 0x00040000,
    STUDIOHDR_FLAGS_SUBDIVISION_SURFACE           = 0x00080000,
    STUDIOHDR_FLAGS_NO_ANIM_EVENTS                = 0x00100000,
    STUDIOHDR_FLAGS_VERT_ANIM_FIXED_POINT_SCALE   = 0x00200000,
};

// ============================================================================
// Main Header (studiohdr_t) - 408 bytes total
// ============================================================================
#pragma pack(push, 1)
struct studiohdr_t {
    int32_t id;                 // Model format ID: "IDST" (0x49 0x44 0x53 0x54)
    int32_t version;            // Format version number
    int32_t checksum;           // Must match PHY and VTX files
    char name[64];              // Internal name of the model, null-padded
    int32_t dataLength;         // Data size of MDL file in bytes

    // Vectors (12 bytes each: 3 floats)
    glm::vec3 eyeposition;      // Position of player viewpoint relative to model origin
    glm::vec3 illumposition;    // Position for ambient light and cubemap reflections
    glm::vec3 hull_min;         // Corner of model hull box with least X/Y/Z values
    glm::vec3 hull_max;         // Opposite corner of model hull box
    glm::vec3 view_bbmin;       // Bounding box min for view culling
    glm::vec3 view_bbmax;       // Bounding box max for view culling

    uint32_t flags;             // Model flags (see MDLFlags)

    // Bone data (mstudiobone_t)
    int32_t bone_count;
    int32_t bone_offset;

    // Bone controller data (mstudiobonecontroller_t)
    int32_t bonecontroller_count;
    int32_t bonecontroller_offset;

    // Hitbox sets (mstudiohitboxset_t)
    int32_t hitbox_count;
    int32_t hitbox_offset;

    // Local animation descriptions (mstudioanimdesc_t)
    int32_t localanim_count;
    int32_t localanim_offset;

    // Local sequence descriptions (mstudioseqdesc_t)
    int32_t localseq_count;
    int32_t localseq_offset;

    int32_t activitylistversion;
    int32_t eventsindexed;

    // Texture data (mstudiotexture_t)
    int32_t texture_count;
    int32_t texture_offset;

    // Texture directory paths (series of null-terminated strings)
    int32_t texturedir_count;
    int32_t texturedir_offset;

    // Skin replacement tables
    int32_t skinreference_count;
    int32_t skinfamily_count;
    int32_t skinreference_index;

    // Body parts (mstudiobodyparts_t)
    int32_t bodypart_count;
    int32_t bodypart_offset;

    // Attachment points (mstudioattachment_t)
    int32_t attachment_count;
    int32_t attachment_offset;

    // Local nodes (single bytes + null-terminated strings)
    int32_t localnode_count;
    int32_t localnode_index;
    int32_t localnode_name_index;

    // Flex descriptors (mstudioflexdesc_t)
    int32_t flexdesc_count;
    int32_t flexdesc_index;

    // Flex controllers (mstudioflexcontroller_t)
    int32_t flexcontroller_count;
    int32_t flexcontroller_index;

    // Flex rules (mstudioflexrule_t)
    int32_t flexrules_count;
    int32_t flexrules_index;

    // IK chains (mstudioikchain_t)
    int32_t ikchain_count;
    int32_t ikchain_index;

    // Mouths (mstudiomouth_t)
    int32_t mouths_count;
    int32_t mouths_index;

    // Local pose parameters (mstudioposeparamdesc_t)
    int32_t localposeparam_count;
    int32_t localposeparam_index;

    // Surface property name (single null-terminated string)
    int32_t surfaceprop_index;

    // Key-value data (series of strings)
    int32_t keyvalue_index;
    int32_t keyvalue_count;

    // IK locks (mstudioiklock_t)
    int32_t iklock_count;
    int32_t iklock_index;

    float mass;                 // Mass in kilograms
    int32_t contents;           // Contents flag

    // Include models (mstudiomodelgroup_t)
    int32_t includemodel_count;
    int32_t includemodel_index;

    int32_t virtualModel;       // Placeholder for mutable void*

    // Animation blocks (mstudioanimblock_t)
    int32_t animblocks_name_index;
    int32_t animblocks_count;
    int32_t animblocks_index;

    int32_t animblockModel;     // Placeholder for mutable void*

    // Bone table name
    int32_t bonetablename_index;

    int32_t vertex_base;        // Placeholder for void*
    int32_t offset_base;        // Placeholder for void*

    uint8_t directionaldotproduct;
    uint8_t rootLod;
    uint8_t numAllowedRootLods;
    uint8_t unused0;
    int32_t unused1;

    // Flex controller UI (mstudioflexcontrollerui_t)
    int32_t flexcontrollerui_count;
    int32_t flexcontrollerui_index;

    float vertAnimFixedPointScale;
    int32_t unused2;

    // Offset to secondary header (studiohdr2_t)
    int32_t studiohdr2index;

    int32_t unused3;

    // Header is 408 bytes long as of SDK2013
};
static_assert(sizeof(studiohdr_t) == 408, "studiohdr_t must be 408 bytes");

// ============================================================================
// Secondary Header (studiohdr2_t)
// ============================================================================
struct studiohdr2_t {
    int32_t srcbonetransform_count;
    int32_t srcbonetransform_index;

    int32_t illumpositionattachmentindex;

    float flMaxEyeDeflection;   // If 0, equivalent to cos(30)

    // Linear bone data (mstudiolinearbone_t)
    int32_t linearbone_index;

    int32_t unknown[64];
};

// ============================================================================
// Texture Structure (mstudiotexture_t) - 64 bytes
// ============================================================================
struct mstudiotexture_t {
    int32_t name_offset;        // Offset for null-terminated string (relative to this struct)
    uint32_t flags;
    int32_t used;               // Padding/runtime
    int32_t unused;             // Padding
    int32_t material;           // Placeholder for IMaterial
    int32_t client_material;    // Placeholder for void*
    int32_t unused2[10];        // Final padding
    // Struct is 64 bytes long
};
static_assert(sizeof(mstudiotexture_t) == 64, "mstudiotexture_t must be 64 bytes");

// ============================================================================
// Bone Structure (mstudiobone_t)
// ============================================================================
struct mstudiobone_t {
    int32_t sznameindex;        // Offset to bone name
    int32_t parent;             // Parent bone index, -1 if root
    int32_t bonecontroller[6];  // Bone controller indices

    glm::vec3 pos;              // Default position
    glm::quat quat;             // Default quaternion
    glm::vec3 rot;              // Default rotation (Radian Euler)

    glm::vec3 posscale;         // Position scale
    glm::vec3 rotscale;         // Rotation scale

    glm::mat4x3 poseToBone;     // 3x4 matrix: transforms default bone pose to local space

    uint32_t qAlignment;        // 
    int32_t flags;              // Bone flags
    int32_t proctype;           // Procedural type
    int32_t procindex;          // Procedural rule index
    int32_t physicsbone;        // Physics bone index

    int32_t surfacepropidx;     // Surface property index
    int32_t contents;           // Contents flag

    int32_t unused[8];          // Unused
};

